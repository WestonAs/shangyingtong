package gnete.etc;

public class BooleanMessage {
	private boolean isSucc;
	private String msg;

	public BooleanMessage(boolean isSucc, String msg) {
		super();
		this.isSucc = isSucc;
		this.msg = msg;
	}
	public BooleanMessage(boolean isSucc) {
		this(isSucc,null);
	}

	
	public boolean isSucc() {
		return isSucc;
	}

	public void setSucc(boolean isSucc) {
		this.isSucc = isSucc;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	@Override
	public String toString() {
		return "RetMsg [isSucc=" + isSucc + ", msg=" + msg + "]";
	}
}
