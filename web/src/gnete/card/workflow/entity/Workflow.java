package gnete.card.workflow.entity;

import java.util.Date;

public class Workflow {
    private String workflowId;

    private String workflowName;

    private String inputLimit;

    private String auditLimit;

    private Date createTime;
    
    private int defaultLevel;

    private String remark;
    
    public String getWorkflowId() {
        return workflowId;
    }

    public void setWorkflowId(String workflowId) {
        this.workflowId = workflowId;
    }

    public String getWorkflowName() {
        return workflowName;
    }

    public void setWorkflowName(String workflowName) {
        this.workflowName = workflowName;
    }

    public String getInputLimit() {
        return inputLimit;
    }

    public void setInputLimit(String inputLimit) {
        this.inputLimit = inputLimit;
    }

    public String getAuditLimit() {
        return auditLimit;
    }

    public void setAuditLimit(String auditLimit) {
        this.auditLimit = auditLimit;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

	public int getDefaultLevel() {
		return defaultLevel;
	}

	public void setDefaultLevel(int defaultLevel) {
		this.defaultLevel = defaultLevel;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}