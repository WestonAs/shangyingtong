package gnete.card.entity;

public class MerchTransRmaDetailKey {
    private String curCode;

    private String payCode;

    private String recvCode;

    private String rmaDate;

    public String getCurCode() {
        return curCode;
    }

    public void setCurCode(String curCode) {
        this.curCode = curCode;
    }

    public String getPayCode() {
        return payCode;
    }

    public void setPayCode(String payCode) {
        this.payCode = payCode;
    }

    public String getRecvCode() {
        return recvCode;
    }

    public void setRecvCode(String recvCode) {
        this.recvCode = recvCode;
    }

    public String getRmaDate() {
        return rmaDate;
    }

    public void setRmaDate(String rmaDate) {
        this.rmaDate = rmaDate;
    }
}