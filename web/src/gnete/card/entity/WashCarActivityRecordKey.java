package gnete.card.entity;

public class WashCarActivityRecordKey {
    private Long activityId;

    private String cardId;

    private String washCarCycle;
    
    public WashCarActivityRecordKey(){
    	
    }
    public WashCarActivityRecordKey(Long activityId,String cardId,String washCarCycle){
    	super();
    	this.activityId = activityId;
    	this.cardId = cardId;
    	this.washCarCycle = washCarCycle;
    }

    public Long getActivityId() {
        return activityId;
    }

    public void setActivityId(Long activityId) {
        this.activityId = activityId;
    }

    public String getCardId() {
        return cardId;
    }

    public void setCardId(String cardId) {
        this.cardId = cardId;
    }

    public String getWashCarCycle() {
        return washCarCycle;
    }

    public void setWashCarCycle(String washCarCycle) {
        this.washCarCycle = washCarCycle;
    }
}