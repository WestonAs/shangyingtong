package gnete.card.entity;

import java.util.Date;

public class BrushSet {
    private Long id;

    private String drawId;

    private Integer prizeNo;

    private String updateUser;

    private Date updateTime;

    private String awdBeginDate;

    private String awdEndDate;

    private Long totalAwdCnt;

    private Long leftAwdCnt;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getDrawId() {
        return drawId;
    }

    public void setDrawId(String drawId) {
        this.drawId = drawId;
    }

    public Integer getPrizeNo() {
        return prizeNo;
    }

    public void setPrizeNo(Integer prizeNo) {
        this.prizeNo = prizeNo;
    }

    public String getUpdateUser() {
        return updateUser;
    }

    public void setUpdateUser(String updateUser) {
        this.updateUser = updateUser;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public String getAwdBeginDate() {
        return awdBeginDate;
    }

    public void setAwdBeginDate(String awdBeginDate) {
        this.awdBeginDate = awdBeginDate;
    }

    public String getAwdEndDate() {
        return awdEndDate;
    }

    public void setAwdEndDate(String awdEndDate) {
        this.awdEndDate = awdEndDate;
    }

    public Long getTotalAwdCnt() {
        return totalAwdCnt;
    }

    public void setTotalAwdCnt(Long totalAwdCnt) {
        this.totalAwdCnt = totalAwdCnt;
    }

    public Long getLeftAwdCnt() {
        return leftAwdCnt;
    }

    public void setLeftAwdCnt(Long leftAwdCnt) {
        this.leftAwdCnt = leftAwdCnt;
    }
}