package gnete.card.entity;

import java.math.BigDecimal;

public class TransCenterMoth {
	private String		month;

	private String		parent;

	private String		branchName;

	private String		transType;

	private BigDecimal	transNum;

	private BigDecimal	amount;

	private BigDecimal	settAmt;

	private BigDecimal	issuerSettAmt;

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getParent() {
		return parent;
	}

	public void setParent(String parent) {
		this.parent = parent;
	}

	public String getBranchName() {
		return branchName;
	}

	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}

	public String getTransType() {
		return transType;
	}

	public void setTransType(String transType) {
		this.transType = transType;
	}

	public BigDecimal getTransNum() {
		return transNum;
	}

	public void setTransNum(BigDecimal transNum) {
		this.transNum = transNum;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public BigDecimal getSettAmt() {
		return settAmt;
	}

	public void setSettAmt(BigDecimal settAmt) {
		this.settAmt = settAmt;
	}

	public BigDecimal getIssuerSettAmt() {
		return issuerSettAmt;
	}

	public void setIssuerSettAmt(BigDecimal issuerSettAmt) {
		this.issuerSettAmt = issuerSettAmt;
	}
	
}