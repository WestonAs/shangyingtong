package gnete.card.entity;

import java.math.BigDecimal;

/**
 * 吉之岛数据统计实体类
 * @author slt02
 *
 */
public class StatisticsCardInfo {
	
	private String statDate;
	
	private String cardIssuer;
	
	private String cardStatus;
	
	private String statMonth;
	
	private BigDecimal totalCardNum;
	
	private BigDecimal totalFaceValue;
	
	private BigDecimal totalBal;

	public String getStatDate() {
		return statDate;
	}

	public void setStatDate(String statDate) {
		this.statDate = statDate;
	}

	public String getCardIssuer() {
		return cardIssuer;
	}

	public void setCardIssuer(String cardIssuer) {
		this.cardIssuer = cardIssuer;
	}

	public String getCardStatus() {
		return cardStatus;
	}

	public void setCardStatus(String cardStatus) {
		this.cardStatus = cardStatus;
	}

	public String getStatMonth() {
		return statMonth;
	}

	public void setStatMonth(String statMonth) {
		this.statMonth = statMonth;
	}

	public BigDecimal getTotalCardNum() {
		return totalCardNum;
	}

	public void setTotalCardNum(BigDecimal totalCardNum) {
		this.totalCardNum = totalCardNum;
	}

	public BigDecimal getTotalFaceValue() {
		return totalFaceValue;
	}

	public void setTotalFaceValue(BigDecimal totalFaceValue) {
		this.totalFaceValue = totalFaceValue;
	}

	public BigDecimal getTotalBal() {
		return totalBal;
	}

	public void setTotalBal(BigDecimal totalBal) {
		this.totalBal = totalBal;
	}
}
