public class MakeCardPersonKey {

	private String branchNo;

	private String userId;

	private String state;

	public MakeCardPersonKey() {

	}

	public MakeCardPersonKey(String branchNo, String state) {
		this();
		this.branchNo = branchNo;
		this.state = state;
	}

	public MakeCardPersonKey(String branchNo, String state, String userId) {
		this(branchNo, state);
		this.userId = userId;
	}

	public String getBranchNo() {
		return branchNo;
	}

	public void setBranchNo(String branchNo) {
		this.branchNo = branchNo;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}
}
