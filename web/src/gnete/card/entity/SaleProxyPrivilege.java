package gnete.card.entity;

public class SaleProxyPrivilege {
    private Long id;

    private String proxyBranch;

    private String cardBranch;

    private String limitId;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getProxyBranch() {
        return proxyBranch;
    }

    public void setProxyBranch(String proxyBranch) {
        this.proxyBranch = proxyBranch;
    }

    public String getCardBranch() {
        return cardBranch;
    }

    public void setCardBranch(String cardBranch) {
        this.cardBranch = cardBranch;
    }

    public String getLimitId() {
        return limitId;
    }

    public void setLimitId(String limitId) {
        this.limitId = limitId;
    }
}